/** Sempre utilizar camelCase
 * 
 * Case-sensitive
 * 
 * CONST QUANDO O VALOR FOR FIXO
 * 
 * Não podemos criar constantes com palavras reservadas
 * Ex - const if, const console, const let.
 * 
 * Constantes precisam ter nomes significativos, que façam sentido com o propósito delas
 * Ex - const nomeCliente, const mediaNotas.
 * 
 * Não pode começar o nome de uma constantes com um número
 * Ex - const 1nome, const 123.
 * 
 * Não podem conter espaços ou traços
 * Ex - const nome-cliente, const numero 1.
 * 
 * Não pode modificar o valor de uma constante nem inicializar ela sem valor
 * 
 * Ex - const nome;
 * 
 * Ex- const cliente = 'Joao';
 *     cliente = 'Miguel';
 *      
 * NÃO UTILIZE VAR, UTILIZE CONST.
 */