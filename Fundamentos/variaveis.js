/** Sempre utilizar camelCase
 * 
 * Case-sensitive
 * 
 * LET QUANDO O VALOR MUDAR
 * 
 * Não podemos criar variáveis com palavras reservadas
 * Ex - let if, let console, let let.
 * 
 * Variáveis precisam ter nomes significativos, que façam sentido com o propósito delas
 * Ex - let nomeCliente, let mediaNotas.
 * 
 * Não pode começar o nome de uma variável com um número
 * Ex - let 1nome, let 123.
 * 
 * Não podem conter espaços ou traços
 * Ex - let nome-cliente, let numero 1.
 * 
 * Não podemos redeclarar variáveis com let mas podemos mudar o valor atribuido
 * 
 * Ex erro - let nome = 'Jose';
 *           let nome;
 * 
 * Ex certo - let nome = 'Jose';
 *            nome = 'Luiz';
 * 
 * NÃO UTILIZE VAR, UTILIZE LET
 * 
 * Diferenças VAR x LET
 * 
 * É possível redeclarar variáveis com VAR
 * Ex - var nome = 'Jose';
 *      var nome = 'Miguel';
 */